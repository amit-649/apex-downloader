// ============================================================
async function extractYouTube(browser) {
  console.log('  🔍 Extracting YouTube cookies...');
  try {
    const page = await browser.newPage();
    await page.goto('https://www.youtube.com', { waitUntil: 'networkidle2', timeout: 30000 });

    const cookies = await page.cookies('https://www.youtube.com', 'https://youtube.com', 'https://google.com');
    await page.close();

    const hasSid = cookies.some(c => c.name === 'SID');
    if (!hasSid) {
      console.log('  ⚠️  YouTube: No active session found.');
      return null;
    }

    // Save cookies.txt for local yt-dlp
    fs.writeFileSync(COOKIES_TXT_PATH, formatNetscapeCookies(cookies), 'utf8');
    console.log(`  ✅ YouTube: Saved cookies.txt`);

    const cookieString = cookies.map(c => `${c.name}=${c.value}`).join('; ');
    updateEnvVar('YOUTUBE_COOKIES', cookieString);
    return cookieString;
  } catch (err) {
    console.log(`  ❌ YouTube extraction error: ${err.message}`);
    return null;
  }
}

async function extractInstagram(browser) {
  console.log('  🔍 Extracting Instagram cookies...');
  try {
    const page = await browser.newPage();
    await page.goto('https://www.instagram.com', { waitUntil: 'networkidle2', timeout: 30000 });

    const cookies = await page.cookies('https://www.instagram.com', 'https://instagram.com');
    await page.close();

    const sessionCookie = cookies.find(c => c.name === 'sessionid');
    if (!sessionCookie || !sessionCookie.value) {
      console.log('  ⚠️  Instagram: No active session found.');
      return null;
    }

    // Save the single sessionid for compatibility
    updateEnvVar('INSTAGRAM_SESSION_ID', sessionCookie.value);

    // Save the full cookie string to bypass bot detection
    const cookieString = cookies.map(c => `${c.name}=${c.value}`).join('; ');
    updateEnvVar('INSTAGRAM_COOKIES', cookieString);

    console.log(`  ✅ Instagram: Extracted active cookies`);
    return cookieString;
  } catch (err) {
    console.log(`  ❌ Instagram extraction error: ${err.message}`);
    return null;
  }
}

