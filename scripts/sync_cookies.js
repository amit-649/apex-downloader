// ============================================================
function syncToVercel(key, value) {
  return new Promise((resolve) => {
    console.log(`☁️  Syncing ${key} to Vercel production...`);
    
    // Remove existing variable if present
    try {
      execSync(`vercel env rm ${key} production --yes`, { stdio: 'ignore', shell: true });
    } catch (e) {
      // Ignored
    }

    // Add new variable using secure stdin piping
    const child = spawn('vercel', ['env', 'add', key, 'production'], { shell: true });
    child.stdin.write(value);
    child.stdin.end();

    child.on('close', (code) => {
      if (code === 0) {
        console.log(`  ✅ Successfully updated ${key} on Vercel!`);
      } else {
        console.log(`  ❌ Failed to update ${key} on Vercel (CLI exit code ${code}).`);
        console.log('     Make sure Vercel CLI is installed (npm i -g vercel) and you are logged in (vercel login).');
      }
      resolve();
    });
  });
}

function deployToVercel() {
  return new Promise((resolve) => {
    console.log('\n🚀 Triggering Vercel production deployment...');
    const child = spawn('vercel', ['--prod', '--yes'], { stdio: 'inherit', shell: true });
    child.on('close', (code) => {
      if (code === 0) {
        console.log('\n🎉 Vercel deployment completed successfully!');
      } else {
        console.log(`\n❌ Vercel deployment failed (exit code ${code}).`);
      }
      resolve();
    });
  });
}

