const puppeteer = require('puppeteer');
const { spawn, execSync } = require('child_process');
const fs = require('fs');
const path = require('path');
const readline = require('readline');

const CHROME_EXE = 'C:\\Program Files\\Google\\Chrome\\Application\\chrome.exe';
const SESSION_DIR = path.join(process.cwd(), 'youtube_session_data');
const COOKIES_TXT_PATH = path.join(process.cwd(), 'cookies.txt');
const ENV_LOCAL_PATH = path.join(process.cwd(), '.env.local');

// Parse CLI Arguments
const args = process.argv.slice(2);
const runBoth = args.includes('--both') || args.length === 0;
const runYt = args.includes('--youtube') || runBoth;
const runIg = args.includes('--instagram') || runBoth;
const syncOnly = args.includes('--sync-only');

function askQuestion(query) {
  const rl = readline.createInterface({ input: process.stdin, output: process.stdout });
  return new Promise((resolve) => rl.question(query, (ans) => { rl.close(); resolve(ans.trim()); }));
}

function formatNetscapeCookies(cookies) {
  let output = '# Netscape HTTP Cookie File\n# Auto-generated by Cookie Scraper\n\n';
  for (const c of cookies) {
    const domain = c.domain;
    const includeSubdomains = domain.startsWith('.') ? 'TRUE' : 'FALSE';
    const cookiePath = c.path || '/';
    const secure = c.secure ? 'TRUE' : 'FALSE';
    const expiry = c.expires ? Math.round(c.expires) : Math.round(Date.now() / 1000 + 365 * 24 * 60 * 60);
    output += `${domain}\t${includeSubdomains}\t${cookiePath}\t${secure}\t${expiry}\t${c.name}\t${c.value}\n`;
  }
  return output;
}

function readEnvLocal() {
  if (fs.existsSync(ENV_LOCAL_PATH)) {
    return fs.readFileSync(ENV_LOCAL_PATH, 'utf8');
  }
  return '';
}

function getEnvVars() {
  const envContent = readEnvLocal();
  const vars = {};
  const lines = envContent.split('\n');
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const match = trimmed.match(/^([^=]+)=(.*)$/);
    if (match) {
      const key = match[1].trim();
      let val = match[2].trim();
      if (val.startsWith('"') && val.endsWith('"')) {
        val = val.substring(1, val.length - 1).replace(/\\"/g, '"');
      } else if (val.startsWith("'") && val.endsWith("'")) {
        val = val.substring(1, val.length - 1);
      }
      vars[key] = val;
    }
  }
  return vars;
}

function updateEnvVar(key, value) {
  let content = readEnvLocal();
  const lines = content.split('\n').filter(line => !line.trim().startsWith(`${key}=`));
  lines.push(`${key}="${value.replace(/"/g, '\\"')}"`);
  fs.writeFileSync(ENV_LOCAL_PATH, lines.join('\n').trim() + '\n', 'utf8');
}

