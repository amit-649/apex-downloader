// Native Chrome Control
// ============================================================
function launchChromeNatively() {
  return new Promise((resolve) => {
    console.log('  🌐 Launching Chrome window (NOT automated)...');
    const child = spawn(CHROME_EXE, [
      `--user-data-dir=${SESSION_DIR}`,
      '--no-first-run',
      '--no-default-browser-check',
      'https://accounts.google.com/ServiceLogin?continue=https%3A%2F%2Fwww.youtube.com%2F'
    ], { detached: true, stdio: 'ignore' });
    child.unref();
    setTimeout(() => resolve(child), 2000);
  });
}

async function killSessionChrome() {
  return new Promise((resolve) => {
    const { exec } = require('child_process');
    exec(`wmic process where "commandline like '%youtube_session_data%'" get processid /format:list`, (err, stdout) => {
      if (err || !stdout.trim()) { resolve(); return; }
      const pids = stdout.match(/ProcessId=(\d+)/g)?.map(m => m.split('=')[1]) || [];
      for (const pid of pids) {
        try { process.kill(parseInt(pid), 'SIGTERM'); } catch (e) { /* ignore */ }
      }
      setTimeout(resolve, 2000);
    });
  });
}

// ============================================================
// Main Pipeline Orchestrator
// ============================================================
async function run() {
  console.log('╔══════════════════════════════════════════════════╗');
  console.log('║   Unified Cookie Refresher & Vercel Sync Tool    ║');
  console.log('╚══════════════════════════════════════════════════╝\n');

  if (!fs.existsSync(CHROME_EXE)) {
    console.error('❌ Google Chrome not found. Check CHROME_EXE path in script.');
    return;
  }

  let ytCookies = null;
  let igSession = null;

  if (syncOnly) {
    console.log('☁️  Skipping browser extraction. Reading cookies from .env.local to sync only...');
    const localVars = getEnvVars();
    ytCookies = localVars['YOUTUBE_COOKIES'];
    igSession = localVars['INSTAGRAM_COOKIES'];
  } else {
    const hasCachedSession = fs.existsSync(SESSION_DIR) && fs.readdirSync(SESSION_DIR).length > 0;

    // --- STEP 1: Attempt Headless Extraction first ---
    if (hasCachedSession) {
      console.log('📦 Cached session found. Attempting headless extraction...');
      try {
        const browser = await puppeteer.launch({
          headless: true,
          executablePath: CHROME_EXE,
          userDataDir: SESSION_DIR,
          args: ['--no-sandbox', '--disable-setuid-sandbox', '--disable-gpu']
        });

        // Run processes in parallel or separately depending on arguments
        const promises = [];
        if (runYt) promises.push(extractYouTube(browser));
        else promises.push(Promise.resolve(null));

        if (runIg) promises.push(extractInstagram(browser));
        else promises.push(Promise.resolve(null));

        const results = await Promise.all(promises);
        ytCookies = results[0];
        igSession = results[1];

        await browser.close();
      } catch (err) {
        console.log('  ⚠️ Headless extraction failed:', err.message);
      }
    }

    // --- STEP 2: Interactive Sign-in Fallback ---
    if ((runYt && !ytCookies) || (runIg && !igSession)) {
      console.log('\n⚠️ Active login session(s) missing or expired.');
      console.log('╔════════════════════════════════════════════════════════╗');
      console.log('║  A normal Chrome window will open.                     ║');
      console.log('║                                                        ║');
      if (runYt) console.log('║  1. Sign in to your Google/YouTube account             ║');
      if (runIg) console.log('║  2. Open a new tab -> go to instagram.com and sign in  ║');
      console.log('║  3. Once logged in, CLOSE Chrome completely            ║');
      console.log('║  4. Come back here and press ENTER                     ║');
      console.log('╚════════════════════════════════════════════════════════╝\n');

      await launchChromeNatively();
      await askQuestion('Press ENTER after you have logged in and CLOSED Chrome...');
      await killSessionChrome();
      await new Promise(r => setTimeout(r, 1000));

      // Try extraction again headlessly
      console.log('\n📦 Extracting refreshed session cookies...');
      const browser = await puppeteer.launch({
        headless: true,
        executablePath: CHROME_EXE,
        userDataDir: SESSION_DIR,
        args: ['--no-sandbox', '--disable-setuid-sandbox', '--disable-gpu']
      });

      const promises = [];
      if (runYt) promises.push(extractYouTube(browser));
      else promises.push(Promise.resolve(null));

      if (runIg) promises.push(extractInstagram(browser));
      else promises.push(Promise.resolve(null));

      const results = await Promise.all(promises);
      ytCookies = results[0];
      igSession = results[1];

      await browser.close();
    }
  }

  // --- STEP 3: Vercel Push ---
  let hasSyncedAny = false;

  if (runYt && ytCookies) {
    await syncToVercel('YOUTUBE_COOKIES', ytCookies);
    hasSyncedAny = true;
  }
  if (runIg && igSession) {
    const sessionIdOnly = igSession.split('; ').find(c => c.startsWith('sessionid='))?.split('=')[1] || '';
    if (sessionIdOnly) {
      await syncToVercel('INSTAGRAM_SESSION_ID', sessionIdOnly);
    }
    await syncToVercel('INSTAGRAM_COOKIES', igSession);
    hasSyncedAny = true;
  }

  // --- STEP 4: Trigger Vercel deploy if anything was synced or syncOnly requested ---
  if (hasSyncedAny || syncOnly) {
    await deployToVercel();
  } else {
    console.log('\n❌ No cookies extracted or synced. Skip deployment.');
  }
}

run().catch(err => console.error('Fatal Error:', err));
